    private void adminLogInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminLogInBtnActionPerformed
        // TODO add your handling code here:
        String id = logId.getText();
        String password = logPassword.getText();
        boolean b = adminList.checkPassword(id, password);
        if(b){
            AdminFrame adminFrame = new AdminFrame();
            adminFrame.setVisible(true);
            adminFrame.eventListMaintain = this.eventList;
            this.setVisible(false);
            adminFrame.loginREgistration = this;
            fillLogField();
            fillRegistrationField();
          
        }
        else{
            logMessage.setText("id and password does not match");
            regMessage.setText("");
        }
    }//GEN-LAST:event_adminLogInBtnActionPerformed

