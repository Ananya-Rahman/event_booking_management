    private void customerRegbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerRegbtnActionPerformed
        String id = regId.getText();
        String name = regName.getText();
        String email = regEmail.getText();
        String phone = regPhone.getText();
        String type = regType.getText();
        String password = regPassword.getText();
        Customer customer = null;
        if(id.isEmpty() || name.isEmpty() || email.isEmpty() || phone.isEmpty() || password.isEmpty()){
            regMessage.setText("Please Fill Up All Field");
            logMessage.setText("");
        }
        else{
            if(type.equals("Local") && type.equals("local")){
                  customer = new LocalCustomer(id, name, phone, email, type, password);
            }
            else if(type.equals("Foreign") && type.equals("foreign")){
                  customer = new ForeignCustomer(id, name, phone, email, type, password);
            }
          
            else{
                customer = new LocalCustomer(id, name, phone, email, type, password);
            }
            customerList.add(customer);
            regMessage.setText("Successfully Customer registered");
            fillRegistrationField();
        }
        
    }//GEN-LAST:event_customerRegbtnActionPerformed

    private void adminRegbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminRegbtnActionPerformed
        // TODO add your handling code here:
        
        String id = regId.getText();
        String name = regName.getText();
        String email = regEmail.getText();
        String phone = regPhone.getText();
        String type = regType.getText();
        String password = regPassword.getText();
        
        Admin admin = null;
        
        if(id.isEmpty() || name.isEmpty() || email.isEmpty() || phone.isEmpty() || password.isEmpty()){
            regMessage.setText("Please Fill Up All Field");
            logMessage.setText("");
        }
        else{
            
            
                if(type.isEmpty()){
                  admin = new Admin(id, name, email, phone, password);
                }
                else{
                    admin = new Admin(id, name, email, phone, type, password);
                }
            adminList.addAdmin(admin);
            regMessage.setText("Successfully Admin registered");
            fillRegistrationField();
        }
        
        
      
        
    }//GEN-LAST:event_adminRegbtnActionPerformed

